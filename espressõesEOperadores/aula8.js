let one = 1;
let two = 2;

// > maior que 
console.log(one > two); //false

// >= maior ou igual a
console.log(one >= 1); //true
console.log(two >= 1);// true

// < menor que 
console.log(one < two); //true

// <= menor ou igual a 
console.log(one <= two);//true
console.log(one <= 1);//true
console.log(one <= 0);//false